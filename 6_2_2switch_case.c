#include <stdio.h>


int main()
{
int degisken=1;
printf("%d\n",degisken);
switch(degisken){
case 1:
    printf("case 1\n");
    break;
case 3:
    printf("case 1\n");
break;
default:
    printf("default\n");
break;
}
    return 0;
}
